def create_bot(name):
  from chatterbot import ChatBot
  Bot = ChatBot(name=name,
                read_only=False, #Chatbot Learns from user input
                logic_adapter=["chatterbot.logoc.BestMatch"],
                storage_adapter="chatterbot.storage.SQLStorageAdapter")
  return Bot

def train_all_data(Bot):
  from chatterbot.trainers import ChatterBotCorpusTrainer
  corpus_trainer = ChatterBotCorpusTrainer(Bot)
  corpus_trainer.train("chatterbot.corpus.english")

def custom_train(Bot,conversation):
  from chatterbot.trainers import ListTrainer
  trainer=ListTrainer(Bot)
  trainer.train(conversation)

def start_chatbot(Bot):
  print('\033c')
  print("Hello, I am Joy. How can I help you?")
  bye_list=["bye joy","bye","good bye"]
  while(True):
    user_input=input("me: ")
    if user_input.lower() in bye_list:
      print("Joy : Good Bye and have a nice day")
      break
    response=Bot.get_response(user_input)
    print("Joy:",response)
#create Chatbot
bot=create_bot("Joy")

#train all data
train_all_data(bot)

#train chatbot with custom data
house_owner=[
  "Who is the owner of the house?",
  "Sankalp Chake"
]
custom_train(bot, house_owner)

#start chatbot
start_chatbot(bot)

